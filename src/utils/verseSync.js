// حساب توقيت تقريبي للآية بناءً على موضعها في السورة
export const calculateVerseTime = (surahNumber, verseNumber, totalVerses, currentTime, totalDuration) => {
  // نسبة الآية من إجمالي الآيات
  const verseRatio = verseNumber / totalVerses;
  
  // التوقيت المتوقع لهذه الآية
  const expectedTime = verseRatio * totalDuration;
  
  return expectedTime;
};

// إيجاد الآية الحالية بناءً على الوقت الحالي
export const getCurrentVerse = (currentTime, totalDuration, totalVerses) => {
  if (!totalDuration || totalDuration === 0) return 0;
  
  // نسبة التقدم في السورة
  const progress = currentTime / totalDuration;
  
  // رقم الآية التقريبي
  const verseIndex = Math.floor(progress * totalVerses);
  
  // التأكد من أن الرقم ضمن النطاق الصحيح
  return Math.min(Math.max(verseIndex, 0), totalVerses - 1);
};
